function makehash(str)
    local n = 0
    for i = 1, #str do
        n = n + string.byte(string.sub(str, i, i)) % #str
    end
    return n
end
function pn()
    local function b(af)
        return (af:sub(1, 1)):upper() .. (af:sub(2, #af))
    end
    local c = names[math.random(1, #names)]
    local a = "%s Potion"
    return string.format(a, b(c)), makehash(string.format(a, b(c))) + (owner.UserId / 1000)
end

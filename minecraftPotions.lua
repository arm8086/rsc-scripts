function eff(char, hash)
    local rand = Random.new(hash)
    local name = char.Name
    local hum = char.Humanoid
    local t = rand:NextInteger(1, 4)
    local t2 = rand:NextInteger(1, 3)
    local oldspeed, oldjp = hum.WalkSpeed, hum.JumpPower
    local length = (t2 == 1 and 30) or (t2 == 2 and 60) or 90
    local t3 = tick()
    local sv, jv, sv2 = rand:NextInteger(4, 30), rand:NextInteger(4, 20), rand:NextInteger(2, 10)
    local spark = Instance.new("Sparkles", char.Head)
    task.spawn(
        function()
            if t == 1 then
                print("Got Speed Effect!")
                hum.WalkSpeed = hum.WalkSpeed + sv
                spark.SparkleColor = Color3.fromRGB(124, 175, 198)
            elseif t == 2 then
                print("Got Jump Boost Effect!")
                hum.JumpPower = hum.JumPower + jv
                spark.SparkleColor = Color3.fromRGB(32, 255, 76)
            elseif t == 3 then
                print("Got Poison Effect!")
                spark.SparkleColor = Color3.fromRGB(78, 147, 49)
                repeat
                    hum.Health = hum.Health - 5
                    hum.Health = math.clamp(hum.Health, 1, 100)
                    task.wait(1)
                until hum.Health == 1 or (tick() - t3) >= length
            elseif t == 4 then
                print("Got Slowness Effect!")
                hum.WalkSpeed = hum.WalkSpeed - sv2
                spark.SparkleColor = Color3.fromRGB(90, 108, 129)
            end
        end
    )
    print("Effect length", length, "seconds")
    repeat
        task.wait()
    until (tick() - t3) >= length
    if t == 1 then
        hum.WalkSpeed = hum.WalkSpeed + sv
    elseif t == 4 then
        hum.WalkSpeed = hum.WalkSpeed + sv2
    elseif t == 2 then
        hum.JumpPower = hum.WalkSpeed - jv
    end
    print("done")
    spark:Destroy()
end

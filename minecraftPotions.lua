function makeTool(boo)
	local tooldelay = tick()
	local name,hash = pn()
	print("Hash:",hash)
	local plr = game:GetService("Players"):GetPlayerFromCharacter(boo.Parent)
	local t = Instance.new("Tool",plr.Backpack)
	t.Name = name
	local pot = makebp()
	pot:SetAttribute("worth",Random.new(hash):NextInteger(0,2^35))
	makesurfaces(pot,hash)
	pot.Name = "Handle"
	pot.Parent = t
	t.Activated:Connect(function()
		if t.Parent:IsA("Model") then
			if tick()-tooldelay >= 1.6 then
				tooldelay = tick()
				task.spawn(function()
					playpotanim(t.Parent,t,hash)
				end)
			end
		end
	end)
	table.insert(potions,t)
end
